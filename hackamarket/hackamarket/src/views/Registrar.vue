      <br />
      <input type="text" placeholder="Image" v-model="newImage" />
      <br />
      <button class="buttonStyle2" @click="deleteInputs()">Cancelar</button>
      <button class="buttonStyle1" @click="registerNewClient()">Agregar cliente</button>
      <div v-show="spinner" class="loader">Loading...</div>
    </div>
  </div>
</template>

<script>
import axios from "axios";
import Swal from "sweetalert2";
export default {
  name: "Registrar",
  data() {
    return {
      newName: "",
      newUsername: "",
      newPassword: "",
      newEmail: "",
      newImage: "",
      spinner: false,
    };
  },
  methods: {
    //ALERT DE EDITAR
    registerClientAlert(username) {
      Swal.fire({
        icon: "success",
        title: "Registro exitoso",
        text: `Bienvenidx ${username}`,
      });
    },
    registerNewClient() {
      const self = this;

      if (
        self.newName === "" ||
        self.newUsername === "" ||
        self.newPassword === "" ||
        self.newEmail === "" ||
        self.newImage === ""
      ) {
        Swal.fire({
          icon: "warning",
          title: "Atencion!",
          text: `Debes llenar todos los datos para completar el registro`,
        });
      } else {
        axios
          .post("http://localhost:3030/register", {
            nombre: self.newName,
            usuario: self.newUsername,
            password: self.newPassword,
            email: self.newEmail,
            foto: self.newImage,
          })
          .then(function (response) {
            console.log(response);
          })
          .catch(function (error) {
            console.log(error);
          });

        this.spinner = true;
        self.registerClientAlert(self.newName);
        setTimeout(function () {
          location.reload();
        }, 2000);
      }
    },
    deleteInputs() {
      const self = this;
      self.newName = "";
      self.newUsername = "";
      self.newPassword = "";
      self.newEmail = "";
      location.reload();
    },
  },
};
</script>

