<template>
  <div>
    <vue-headful title="Registro | HackaMarket" description="Registro nuevo usuario" />

    <h1 class="greenTitle">Nuevo cliente:</h1>
    <div class="registerContent">
      <input type="text" placeholder="Nombre" v-model="newName" />
      <br />
